HOLIDAYS.nextHoliday = function(hdate){
        var i;
        var y = hdate.getYear();
        var fn;
        var holiday = null;
       
        while(true){
                for(i = 0; i < HOLIDAYS.events.length; i ++){
                        fn = HOLIDAYS.events[i];                       
                        holiday = fn(y, true);
                        if(holiday.hStartDate.gt(hdate)){
                                return new JEvent(i, new GDate(holiday.hStartDate), new GDate(holiday.hEndDate));
						}
                }
                y ++;
        }
}
